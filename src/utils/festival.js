export function getFestival(date) {
  let mDate, lunarFestival, solarFestival, term;
  let lunarDate = solarLunarConverter.solorToLunar(date);
  if (lunarDate) {
    mDate = moment(lunarDate);
    let lunarMonth = mDate.format('MM');
    let lunarDay = mDate.format('DD');
    lunarFestival = LUNAR__FESTIVAL[`${lunarMonth}${lunarDay}`];
  }
  if (!lunarFestival) {
    mDate = moment(date);
    let solarMonth = mDate.format('MM');
    let solarDay = mDate.format('DD');
    solarFestival = SOLAR_FESTIVAL[`${solarMonth}${solarDay}`];
  }
  if (!lunarFestival && !solarFestival) {
    term = getTerm(date);
  }
  return lunarFestival || solarFestival || term;
}

function getTerm(date) {
  let mDate = moment(date);
  let year = mDate.year();
  let month = mDate.month() + 1;
  let day = mDate.date();
  let firstTermDay = getTerm2Day(year, (month * 2 - 1));
  if (firstTermDay === day) {
    return SOLAR_TERM[month * 2 - 2];
  }
  let secondTermDay = getTerm2Day(year,(month * 2));
  if (secondTermDay === day) {
    return SOLAR_TERM[month * 2 - 1];
  }
  return null;
}

/**
 * @param {*} year
 * @param {*} index 二十四节气中的第几个节气
 */
function getTerm2Day(year, index) {
  if (year < 1900 || year > 2100 || index < 1 || index > 24) return -1;
  let table = S_TERM_INFO[year - 1900];
  let terms = [
    parseInt('0x' + table.substr(0, 5)).toString(),
    parseInt('0x' + table.substr(5, 5)).toString(),
    parseInt('0x' + table.substr(10, 5)).toString(),
    parseInt('0x' + table.substr(15, 5)).toString(),
    parseInt('0x' + table.substr(20, 5)).toString(),
    parseInt('0x' + table.substr(25, 5)).toString()
  ];

  // 24 节气对应的日
  let group1 = terms[0], group2 = terms[1], group3 = terms[2], group4 = terms[3],
    group5 = terms[4], group6 = terms[5];
  let term2Days = [
    group1.substr(0, 1), group1.substr(1, 2), group1.substr(3, 1), group1.substr(4, 2),
    group2.substr(0, 1), group2.substr(1, 2), group2.substr(3, 1), group2.substr(4, 2),
    group3.substr(0, 1), group3.substr(1, 2), group3.substr(3, 1), group3.substr(4, 2),
    group4.substr(0, 1), group4.substr(1, 2), group4.substr(3, 1), group4.substr(4, 2),
    group5.substr(0, 1), group5.substr(1, 2), group5.substr(3, 1), group5.substr(4, 2),
    group6.substr(0, 1), group6.substr(1, 2), group6.substr(3, 1), group6.substr(4, 2)
  ];

  return parseInt(term2Days[index - 1]);
}